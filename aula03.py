# Verifica a maior idade  

# passo 1 - Criar a variavel 
# passo 2 - cria uma estrutura de condição para veridicar a maior idade
# passo 3 - imprimir o resultado

idade  = int(input("Informe a sua idade: "))

if idade >= 18:
    print("Você é maior de idade")

elif idade >= 12:
    print("Você é adolescente")

else:
    print("Você é criança")

    
